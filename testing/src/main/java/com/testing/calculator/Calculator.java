package com.testing.calculator;

public class Calculator {
	int a ,b;
	
	public Calculator() {
		this.a = a;
		this.b = b;
	}
	
	public int add(int a, int b){
		return a+b;
	}
	
	public int substract(int a, int b){
		return a-b;
	}
	
	public int exp(int a){
		return a*a;
	}
	
	public int division (int a, int b){
		return a/b;
	}
	
	
	public int multiplication (int a, int b){
		return a*b;
	}
}
